package com.centris.campus.pojo;

public class LibraryItems {
	private String itemtype;
	private String id;
	private String name;
	private String description;
	private String status;
	private String craetedby;
	private String createdtime;
	private String updateby;
	private String updatetime;
	private String log_audit_session;
	
	
	public String getLog_audit_session() {
		return log_audit_session;
	}
	public void setLog_audit_session(String log_audit_session) {
		this.log_audit_session = log_audit_session;
	}
	public String getCraetedby() {
		return craetedby;
	}
	public void setCraetedby(String craetedby) {
		this.craetedby = craetedby;
	}
	public String getCreatedtime() {
		return createdtime;
	}
	public void setCreatedtime(String createdtime) {
		this.createdtime = createdtime;
	}
	public String getUpdateby() {
		return updateby;
	}
	public void setUpdateby(String updateby) {
		this.updateby = updateby;
	}
	public String getUpdatetime() {
		return updatetime;
	}
	public void setUpdatetime(String updatetime) {
		this.updatetime = updatetime;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getItemtype() {
		return itemtype;
	}
	public void setItemtype(String item) {
		this.itemtype = item;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
